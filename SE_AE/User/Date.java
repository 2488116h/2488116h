package User;

public class Date {
	private int dob_d;
	private int dob_m;
	private int dob_y;	
	public Date(int d, int m, int y) {
			dob_d = d;
	        dob_m = m;
	        dob_y = y;
	}
	public int getDob_d() {
		return dob_d;
	}
	public int getDob_m() {
		return dob_m;
	}
	public int getDob_y() {
		return dob_y;
	}
	
	
}
